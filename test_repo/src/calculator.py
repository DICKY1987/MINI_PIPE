"""
Simple calculator module for ACMS testing.

This module intentionally has gaps for ACMS to discover:
- Missing docstrings in some functions
- No input validation
- No error handling
- Missing edge case tests
"""


def add(a, b):
    """Add two numbers."""
    return a + b


def subtract(a, b):
    # Missing docstring
    return a - b


def multiply(a, b):
    # Missing docstring and validation
    return a * b


def divide(a, b):
    # Missing docstring, validation, and error handling
    return a / b
